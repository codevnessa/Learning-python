# Explicação do conteúdo: A função len() retorna o número de elementos na lista, o operador in verifica se um valor está presente na lista, e o operador not in verifica se um valor não está presente.

# Exemplo de uso de len(), in e not in:
lista = [10, 20, 30]
tamanho = len(lista)  # Retorna o número de elementos na lista
print("Tamanho da lista:", tamanho)

print("20 está na lista?", 20 in lista)  # Verifica se 20 está na lista
print("40 não está na lista?", 40 not in lista)  # Verifica se 40 não está na lista

# Explicação do código:
# 1. len() retorna o número de elementos na lista.
# 2. in verifica se um valor está presente na lista.
# 3. not in verifica se um valor não está presente na lista.
# OBS: in e not in são úteis para verificações rápidas.