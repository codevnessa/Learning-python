# Explicação do conteúdo: Tuplas podem ser concatenadas (combinadas) ou repetidas para criar novas tuplas.
# A concatenação usa o operador +, e a repetição usa o operador *.

# Exemplo de concatenação e repetição de tuplas:
tupla1 = (1, 2, 3)
tupla2 = (4, 5, 6)
nova_tupla = tupla1 + tupla2  # Concatena as duas tuplas
print("Tupla concatenada:", nova_tupla)

tupla_repetida = tupla1 * 2  # Repete a tupla1 duas vezes
print("Tupla repetida:", tupla_repetida)

# Explicação do código:
# 1. Duas tuplas (tupla1 e tupla2) são concatenadas usando o operador +.
# 2. A tupla1 é repetida duas vezes usando o operador *.
# OBS: A concatenação e repetição criam novas tuplas, sem modificar as originais.