# Explora métodos úteis de conjuntos, como add, discard e len

# Criando um conjunto
animais = {"gato", "cachorro", "pássaro"}

# Adicionando um item
animais.add("peixe")

# Removendo um item
animais.discard("pássaro")

# Verificando o tamanho do conjunto
tamanho = len(animais)

# Exibindo o conjunto e o tamanho
print(animais)
print("Tamanho do conjunto:", tamanho)

"""
- animais = {"gato", "cachorro", "pássaro"}: Cria um conjunto com três animais.
- animais.add("peixe"): Adiciona "peixe" ao conjunto.
- animais.discard("pássaro"): Remove "pássaro" do conjunto.
- tamanho = len(animais): Calcula o tamanho do conjunto.
- print(animais) e print("Tamanho do conjunto:", tamanho): Exibe o conjunto e seu tamanho.
"""