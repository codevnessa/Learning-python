# Explicação do conteúdo: Uma matriz é uma lista de listas, onde cada lista interna representa uma linha da matriz.
# Podemos acessar elementos específicos usando índices duplos (linha e coluna).

# Exemplo de criação e acesso a uma matriz:
matriz = [[1, 2, 3], [4, 5, 6], [7, 8, 9]]  # Cria uma matriz 3x3
print("Matriz completa:", matriz)

elemento = matriz[1][2]  # Acessa o elemento na segunda linha e terceira coluna
print("Elemento na linha 1, coluna 2:", elemento)

# Explicação do código:
# 1. A matriz é criada como uma lista de listas.
# 2. O elemento na segunda linha e terceira coluna é acessado usando matriz[1][2].
# OBS: O primeiro índice representa a linha, e o segundo representa a coluna.