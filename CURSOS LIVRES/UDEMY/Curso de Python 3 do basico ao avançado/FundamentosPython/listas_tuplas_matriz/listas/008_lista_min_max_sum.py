# Explicação do conteúdo: As funções min(), max() e sum() retornam o menor valor, o maior valor e a soma de todos os elementos da lista, respectivamente.

# Exemplo de uso de min(), max() e sum():
lista = [10, 20, 30]
menor = min(lista)  # Retorna o menor valor da lista
print("Menor valor:", menor)

maior = max(lista)  # Retorna o maior valor da lista
print("Maior valor:", maior)

soma = sum(lista)  # Retorna a soma de todos os elementos da lista
print("Soma dos valores:", soma)

# Explicação do código:
# 1. min() retorna o menor valor da lista.
# 2. max() retorna o maior valor da lista.
# 3. sum() retorna a soma de todos os elementos da lista.
# OBS: sum() só funciona com listas de números.