# Explicação do conteúdo: O while é uma estrutura de repetição que executa um bloco de código enquanto uma condição for verdadeira.
# Ele é útil quando você não sabe quantas vezes o loop precisa ser executado, mas sabe a condição que deve ser atendida para continuar.

# Exemplo de uso do while:
contador = 0  # Inicializa o contador com 0

while contador < 5:  # Enquanto o contador for menor que 5
    print("Contador:", contador)  # Imprime o valor atual do contador
    contador += 1  # Incrementa o contador em 1

# Explicação do código:
# 1. O contador começa em 0.
# 2. O loop while continua enquanto o contador for menor que 5.
# 3. A cada iteração, o valor do contador é impresso.
# 4. O contador é incrementado em 1 a cada iteração.
# 5. O loop termina quando o contador atinge 5.
# OBS: O while é ideal para situações onde o número de iterações não é conhecido antecipadamente.