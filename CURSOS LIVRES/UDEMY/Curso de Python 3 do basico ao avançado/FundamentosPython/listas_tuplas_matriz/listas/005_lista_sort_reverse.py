# Explicação do conteúdo: O método sort() ordena os elementos da lista em ordem crescente (ou alfabética, para strings), enquanto o método reverse() inverte a ordem dos elementos.

# Exemplo de uso de sort() e reverse():
lista = [30, 10, 20]
lista.sort()  # Ordena a lista em ordem crescente
print("Lista após sort:", lista)

lista.reverse()  # Inverte a ordem dos elementos da lista
print("Lista após reverse:", lista)

# Explicação do código:
# 1. sort() ordena a lista em ordem crescente.
# 2. reverse() inverte a ordem dos elementos da lista.
# OBS: sort() e reverse() modificam a lista original.