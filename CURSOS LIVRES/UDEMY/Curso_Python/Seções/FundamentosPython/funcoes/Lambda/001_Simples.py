# Funções lambda são funções anônimas e concisas, ideais para tarefas simples.

saudacao = lambda: "Olá, mundo!"  # Define uma função lambda que retorna "Olá, mundo!"
mensagem = saudacao()  # Chama a função e armazena o resultado
print(mensagem)  # Exibe o resultado: "Olá, mundo!"

# - `lambda:` cria uma função sem parâmetros.
# - `"Olá, mundo!"` é o valor retornado pela função.
# - A função é atribuída à variável `saudacao` e chamada posteriormente.