# Explicação do conteúdo: Tuplas são estruturas de dados imutáveis que armazenam coleções de itens.
# Elas podem ser criadas usando parênteses () ou apenas vírgulas. Os itens podem ser acessados por índices.

# Exemplo de criação e acesso a uma tupla:
tupla = (1, 2, 3)  # Cria uma tupla usando parênteses
print("Tupla:", tupla)  # Imprime a tupla completa
print("Primeiro elemento:", tupla[0])  # Acessa o primeiro elemento (índice 0)
print("Último elemento:", tupla[-1])  # Acessa o último elemento (índice -1)

# Explicação do código:
# 1. A tupla é criada com os valores 1, 2 e 3.
# 2. O primeiro elemento é acessado usando tupla[0].
# 3. O último elemento é acessado usando tupla[-1].
# OBS: Tuplas são imutáveis, ou seja, seus elementos não podem ser alterados após a criação.