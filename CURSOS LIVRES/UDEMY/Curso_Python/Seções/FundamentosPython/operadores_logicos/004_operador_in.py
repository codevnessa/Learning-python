# Operador IN
# O operador IN verifica se um valor está presente em uma sequência (como listas, tuplas, strings, etc.). Ele retorna True se o valor estiver na sequência, e False caso contrário.

# Exemplo prático:
lista = [1, 2, 3, 4, 5]

# Verifica se o número 3 está na lista
if 3 in lista:
    print("O número 3 está na lista.")
else:
    print("O número 3 não está na lista.")

# Explicação:
# 3 in lista → True (o número 3 está na lista).
# Como a expressão é True, o programa imprime "O número 3 está na lista.".