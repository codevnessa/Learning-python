# Criar uma função lambda que recebe um valor e faz algo com ele:

# Função lambda que dobra um número
dobrar = lambda x: x * 2

# Usando a função lambda
resultado = dobrar(5)
print(resultado)  # Resultado: 10


# 1. `lambda x:` cria uma função lambda que recebe um valor (x).
# 2. `x * 2` é o que a função faz: pega o valor de x e multiplica por 2.
# 3. `dobrar(5)` chama a função lambda, passando o número 5 como x.
# 4. O resultado (10) é armazenado na variável `resultado` e exibido.