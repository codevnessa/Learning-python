# Realiza operações de união, interseção e diferença usando métodos de conjuntos

# Definindo dois conjuntos
A = {1, 2, 3}
B = {3, 4, 5}

# União
uniao = A.union(B)

# Interseção
intersecao = A.intersection(B)

# Diferença
diferenca = A.difference(B)

# Exibindo os resultados
print("União:", uniao)
print("Interseção:", intersecao)
print("Diferença:", diferenca)

"""
- A = {1, 2, 3} e B = {3, 4, 5}: Definindo dois conjuntos.
- uniao = A.union(B): Calcula a união de A e B.
- intersecao = A.intersection(B): Calcula a interseção de A e B.
- diferenca = A.difference(B): Calcula a diferença entre A e B.
- print(...): Exibe os resultados das operações.
"""