# Criar uma função lambda que recebe mais de um valor:


# Função lambda que soma dois números
somar = lambda a, b: a + b

# Usando a função lambda
resultado = somar(3, 7)
print(resultado)  # Resultado: 10


# 1. `lambda a, b:` cria uma função lambda que recebe dois valores (a e b).
# 2. `a + b` é o que a função faz: soma os dois valores.
# 3. `somar(3, 7)` chama a função lambda, passando 3 e 7 como valores.
# 4. O resultado (10) é exibido.