# Valor None
# None é um valor especial em Python que representa a ausência de valor ou um valor nulo. Ele é frequentemente usado para inicializar variáveis que ainda não têm um valor definido.

# Sintaxe:
# variavel = None

# Exemplo prático:
resultado = None

if resultado is None:
    print("A variável ainda não tem valor.")
else:
    print("O valor da variável é:", resultado)

# Explicação:
# A variável resultado é inicializada com None.
# O programa verifica se resultado é None usando o operador is.
# Se for None, exibe a mensagem "A variável ainda não tem valor.".