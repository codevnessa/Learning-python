# Explicação do conteúdo: O for in range é uma variação do for que itera sobre uma sequência numérica gerada pela função range().
# Ele é útil quando você quer repetir um bloco de código um número específico de vezes.

# Exemplo de uso do for in range:
for i in range(5):  # Para cada número no intervalo de 0 a 4
    print("Número:", i)  # Imprime o valor atual de i

# Explicação do código:
# 1. A função range(5) gera uma sequência de números de 0 a 4.
# 2. O loop for percorre cada número na sequência.
# 3. A cada iteração, o valor de i é impresso.
# 4. O loop termina quando todos os números da sequência são percorridos.
# OBS: O for in range é ideal para repetir um bloco de código um número específico de vezes.