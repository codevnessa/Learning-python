# Criando uma lista com letras maiúsculas
maiusculas = [x.upper() for x in ['a', 'b', 'c']]

# Exibindo a lista
print(maiusculas)  # Resultado: ['A', 'B', 'C']

# 1. `[x.upper() for x in ['a', 'b', 'c']]` converte letras para maiúsculas.
# 2. `x.upper()` faz a conversão.
# 3. `print(maiusculas)` exibe a lista.