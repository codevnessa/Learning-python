# Explicação do conteúdo: O for é uma estrutura de repetição usada para iterar sobre uma sequência (como uma lista, string ou range).
# Ele é útil quando você sabe quantas vezes o loop precisa ser executado ou quando quer percorrer uma coleção de itens.

# Exemplo de uso do for:
frutas = ["maçã", "banana", "laranja"]  # Lista de frutas

for fruta in frutas:  # Para cada fruta na lista
    print("Fruta:", fruta)  # Imprime o nome da fruta

# Explicação do código:
# 1. A lista `frutas` contém três elementos.
# 2. O loop for percorre cada item da lista.
# 3. A cada iteração, o valor da fruta atual é impresso.
# 4. O loop termina quando todos os itens da lista são percorridos.
# OBS: O for é ideal para iterar sobre coleções ou quando o número de iterações é conhecido.