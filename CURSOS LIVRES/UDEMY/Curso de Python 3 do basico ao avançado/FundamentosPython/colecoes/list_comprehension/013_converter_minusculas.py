# Criando uma sequência com letras minúsculas
minusculas = [x.lower() for x in ['A', 'B', 'C']]

# Exibindo a sequência
print(minusculas)  # Resultado: ['a', 'b', 'c']

# 1. `[x.lower() for x in ['A', 'B', 'C']]` converte letras para minúsculas.
# 2. `x.lower()` faz a conversão.
# 3. `print(minusculas)` exibe a sequência.