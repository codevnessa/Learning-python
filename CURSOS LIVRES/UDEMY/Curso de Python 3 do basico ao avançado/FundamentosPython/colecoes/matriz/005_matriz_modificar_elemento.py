# Explicação do conteúdo: Podemos modificar um elemento específico da matriz atribuindo um novo valor ao índice desejado.

# Exemplo de modificação de um elemento:
matriz = [[1, 2, 3], [4, 5, 6], [7, 8, 9]]

matriz[0][1] = 99  # Modifica o elemento na primeira linha e segunda coluna
print("Matriz após modificar elemento:", matriz)

# Explicação do código:
# 1. matriz[0][1] = 99 altera o valor na primeira linha e segunda coluna para 99.
# OBS: A matriz é mutável, então os elementos podem ser alterados diretamente.